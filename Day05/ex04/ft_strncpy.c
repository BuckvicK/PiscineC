char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int			i;
	unsigned	k;

	i = 0;
	k = 0;
	while (*(src + i) != '\0' && k < n)
	{
		*(dest + i) = *(src + i);
		i++;
		k++;
	}
	*(dest + i) = '\0';
	return (dest);
}
